public class Logical {
    public static void main(String[] args) {
        System.out.println((3>2) && (5>0));//true case
        System.out.println((3<2) || (5<0));//false case
        System.out.println(!(3<2));
    }
}
/* Logical Operator
-> And Operator (&&)
-> Or Operator(||)
-> Not Operator(!)
*/