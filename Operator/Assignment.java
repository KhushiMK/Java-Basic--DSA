public class Assignment {
    public static void main(String[] args) {
        int a = 10;
        a+=10;
        System.out.println(a);
        a-=5;
        System.out.println(a);
        a*=5;
        System.out.println(a);
        a/=5;
        System.out.println(a);
        a%=5;
        System.out.println(a);
    }
}
/* Assignment Operator
-> equal to (=)
-> a = a+10 (a+= 10)
-> a = a-4 (a-= 4)
-> a= a*3 (a*=3)
->a = a/5 (a/=5)
->a = a%3(a)
*/